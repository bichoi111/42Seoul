int		ft_atoi(char *str)
{
	int		m_cnt;
	int		ret;

	m_cnt = 0;
	ret = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			m_cnt++;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		ret = ret * 10 + (*str - '0');
		str++;
	}
	return (m_cnt % 2 ? -ret : ret);
}
